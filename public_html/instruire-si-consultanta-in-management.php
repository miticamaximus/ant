                <h2><a name="4"></a>Consultantă pentru implementarea sistemului de management de mediu conform SR EN ISO 14001:2005</h2>
<h3>Pentru proiectarea şi implementarea unui sistem de management de mediu este necesara parcurgerea urmatoarelor etape:</h3>

<h3>Audit iniţial </h3>
<p>Auditul se efectuează în scopul evaluării şi diagnosticării situaţiei existente, faţă de cerinţele standardului de referinţă SR EN ISO 14001:2005.  </p>

<h3>1.	Planificare</h3>
<p>Asistenţă tehnică la elaborarea politicii sistemului de management, identificarea şi documentarea obiectivelor, termenelor de realizare şi responsabilităţilor, identificarea proceselor şi a interacţiunii acestora.</p>

<h3>2.	Instruire</h3>
<p>Se efectuează la sediul clientului şi cuprinde următoarele cursuri:</p>
<ul>
<li>Managementul de mediu. Principii generale SR EN ISO 14001:2005. (Curs pentru iniţierea personalului în problematica standardului de referinţă).</li>
<li>Managementul de mediu. Documentaţie. (Curs pentru elaborarea, menţinerea, îmbunătăţirea şi ţinerea sub control a documentaţiei)</li>
</ul>
<h3>3.	Elaborarea  documentaţiei </h3>
<p>Se elaborează documentaţia sistemului, care cuprinde:  manual de mediu, proceduri de sistem conform cerinţelor SR EN ISO 14001:2005,  integrarea procedurilor operationale / instructiuni cu cerintele de mediu, descrierea proceselor şi interacţiunii acestora, lista procedurilor / instructiunilor aplicabile, asistenta tehnica la identificarea aspectelor de mediu si la elaborarea programului de management de mediu aferent.</p>

<h3>4.	Audit de implementare</h3>
<p>Se efectuează o evaluare a stadiului implementării sistemului de management de mediu, în acelaşi timp, cu instruirea personalului privind implementarea sistemului.</p>

<h3>5.	Audit final</h3>
<p>Urmărirea acţiunilor corective şi imbunătăţirea sistemului</p>

