<h2><a name="3"></a>Consultanţă pentru implementarea sistemului de management integrat calitate-mediu- sănătate şi securitate ocupaţională</h2>

<p>Pentru proiectarea şi implementarea unui sistem de management integrat este necesara parcurgerea următoarelor etape:</p>

<h3>1. Audit initial</h3>
<p>Auditul se efectuează în scopul evaluarii şi diagnosticării situaţiei existente, fată de cerintele standardelor de referinţă SR EN ISO 9001:2008, SR EN ISO 14001:2005 şi respectiv SR OHSAS 18001:2008.</p>

<h3>2. Planificare</h3>
<p>Asistentă tehnică la elaborarea politicii sistemului de management integrat, identificarea şi documentarea obiectivelor / ţintelor, termenelor de realizare şi responsabilităţilor.</p>

<h3>3. Instruire</h3>
<p>Se efectuează la sediul clientului şi cuprinde următoarele cursuri:</p>
<ul>
<li>Managementul calităţii , de mediu, sanatăţii şi securităţii ocupaţionale. Principii generale SR EN ISO 9001:2008, SR EN ISO 14001:2005 si SR OHSAS 18001:2008 Curs pentru iniţierea personalului în problematica standardelor de referinţă).</li>
<li>Managementul calităţii , de mediu, sanatăţii şi securităţii ocupaţionale. Documentaţie (Curs pentru elaborarea, menţinerea, îmbunatăţirea şi ţinerea sub control a documentaţiei sistemului de management integrat).</li>

<li>Identificare aspecte de mediu (Se elaboreaza Analiza Iniţială de Mediu pentru identificare tuturor aspectelor de mediu şi selectarea celor cu impact major asupra mediului).</li>
</ul>
<h3>4. Elaborarea documentaţiei</h3>
<p>Se elaborează documentaţia sistemului, care cuprinde: manual integrat calitate – mediu – sanătate şi securitate ocupaţională, proceduri de sistem conform cerinţelor standardelor de referinţă, identificarea şi descrierea proceselor şi interacţiunea acestora, identificarea aspectelor de mediu, programe de management, lista procedurilor aplicabile.</p>

<h3>5. Audit de implementare</h3>
<p>Se efectuează o evaluare a stadiului implementării sistemului de management integrat, în acelaşi timp, cu instruirea personalului privind implementarea sistemului.</p>

<h3>6. Audit final</h3>
<p>Urmărirea acţiunilor corective şi imbunătaţirea sistemului</p>

