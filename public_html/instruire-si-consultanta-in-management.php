                <h2><a name="1"></a>Consultanţă pentru implementarea sistemului de management al calităţii conform SR EN ISO 9001:2008</h2>

<p><strong>Pentru proiectarea şi implementarea unui sistem de management al calităţii este necesara parcurgerea urmatoarelor etape:</strong></p>

<h3>1. Audit initial</h3>
<p>Auditul se efectuează în scopul evaluarii şi diagnosticării situatiei existente, fată de cerintele standardului de referintă SR EN ISO 9001:2008.</p>

<h3>2. Planificare</h3>
<p>Asistenţă tehnică la elaborarea politicii sistemului de management, identificarea şi documentarea obiectivelor, termenelor de realizare şi responsabilităţilor, identificarea proceselor şi a interacţiunii acestora.</p>

<h3>3. Instruire</h3>
<p>Se efectuează la sediul clientului şi cuprinde următoarele cursuri:</p>
<ul>
<li>Managementul calităţii. Principii generale SR EN ISO 9001:2008. (Curs pentru initierea personalului in problematica standardului de referinta).</li>
<li>Managementul calităţii. Documentaţie. (Curs pentru elaborarea, menţinerea, imbunătaţirea şi ţinerea sub control a documentaţiei)</li>
</ul>
<h3>4. Elaborarea documentatiei</h3>
<p>Se elaborează documentaţia sistemului, care cuprinde: manualul calităţii, proceduri de sistem conform cerintelor SR EN ISO 9001:2008, descrierea proceselor şi interactiunii acestora, lista procedurilor aplicabile.</p>
<h3>5. Audit de implementare</h3>
<p>Se efectuează o evaluare a stadiului implementării sistemului de management al calităţii, în acelaşi timp, cu instruirea personalului privind implementarea sistemului.</p>
<h3>6. Audit final</h3>
<p>Urmărirea acţiunilor corective şi îmbunătăţirea sistemului</p>
<h3>7. Asistentă tehnică la certificare</h3>
<p>Se asigură gratuit pe perioada desfaşurării auditului de certificare.</p>

<p><strong>Pentru a va comunica oferta financiară, va rugam sa completaţi şi să ne transmiteţi cererea de <a href="#">ofertă alaturată</a>.</strong></p><br />
